package libraryManagementSystem;

public class Library {

	private int id;

	private int noOfRacks;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getNoOfRacks() {
		return noOfRacks;
	}

	public void setNoOfRacks(int noOfRacks) {
		this.noOfRacks = noOfRacks;
	}

}
